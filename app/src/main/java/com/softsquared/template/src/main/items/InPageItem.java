package com.softsquared.template.src.main.items;

public class InPageItem {
    public String a, b, c;

    public InPageItem(String p_a, String p_b, String p_c) {
        this.a = p_a;
        this.b = p_b;
        this.c = p_c;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }
}
